public class Cargo {

    private String name;

    private int num;

    private String type;

    public void setName(String name) {
        this.name = name;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public int getNum() {
        return num;
    }

    public String getType() {
        return type;
    }
}
